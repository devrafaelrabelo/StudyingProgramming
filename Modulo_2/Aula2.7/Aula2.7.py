"""

str - String

"""


print('Teste Aspas Simples')  # Aspas Simples
print("Teste Aspas Duplas")  # Aspas Duplas
print(12356)  # Numero Inteiro


print('Essa é uma "string" (str).')
print("Essa é uma 'string' (str).")
print("Essa é uma \"string\" (str).")  # Barra Invertida antes serve para printar varias aspas dentro do print
print('Essa é uma \'string\' (str).')
print('Essa é uma \n \'string\' (str).')  # Barra Invertida + N - (\n), Serve para quebra de linha.
print(r'Essa é uma \n \'string\' (str).')  # r antes das aspas, serve para printar sem nenhuma função.

